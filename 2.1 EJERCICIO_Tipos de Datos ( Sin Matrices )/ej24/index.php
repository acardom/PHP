<?php
// Declaramos una variable de tipo flotante
$numero_flotante = 10.5;

// Declaramos una variable de tipo entero
$numero_entero = 4;

// Realizamos una operación de suma
$resultado = $numero_flotante + $numero_entero;

echo "Número flotante: " . $numero_flotante . "\n";
echo "Número entero: " . $numero_entero . "\n";
echo "Resultado de la suma: " . $resultado . "\n";

// También podemos realizar una operación de multiplicación
$resultado_multiplicacion = $numero_flotante * $numero_entero;
echo "Resultado de la multiplicación: " . $resultado_multiplicacion . "\n";
?>
