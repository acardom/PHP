<?php
// Declaramos una variable de tipo cadena
$cadena = "Hola, Mundo!";

// Convertimos la cadena a mayúsculas
$mayusculas = strtoupper($cadena);
echo "En mayúsculas: " . $mayusculas . "\n";

// Convertimos la cadena a minúsculas
$minusculas = strtolower($cadena);
echo "En minúsculas: " . $minusculas . "\n";
?>
